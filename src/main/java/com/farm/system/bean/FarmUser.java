package com.farm.system.bean;

public class FarmUser {

	private Long id;
	
	private String framNo;
	
	private String userNo;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getFramNo() {
		return framNo;
	}

	public void setFramNo(String framNo) {
		this.framNo = framNo;
	}

	public String getUserNo() {
		return userNo;
	}

	public void setUserNo(String userNo) {
		this.userNo = userNo;
	}
	
	
}
